# upper()     #Uppercase
# lower()     #Lowercase
# append()    #Append content
# len()       #Determine length in an dataset

# Array
name = "Anthony"
print(name.lower())
print(name.upper())
print(len(name))

schooling = "class"
print(len(schooling))

# Append()
fruits =  ["mangoes", "banana", "oranges"]
fruits.append("apples")
print(fruits)
print(len(fruits))

#Boolean
print( 10 == 9)
print( 10 != 9)
print( 10 > 9)

b = 19
if b >= 30:
    print("A")
elif b >= 20:
    print("B")
else:
    print("C")
